	private Currency(int value, String code, String html ) {
		this.value = value;
		this.code = code;
		this.html = html;
	}
	
	public int getValue() {
		return this.value;
	}
	
	public String getISOValue() {
		if( this.value < 10 )
			return "00" + this.value;
		if( this.value < 100 )
			return "0" + this.value;
		return Integer.toString( this.value );
	}
	
	public String getHtml() {
		return this.html;
	}
	
	public static Currency fromCode( int code ) {
		for( Currency c : Currency.values() ) {
			if( c.value == code )
				return c;
		}
		return null;
	}
	
	public static Currency fromCode( String code ) {
		for( Currency c : Currency.values() ) {
			if( c.code.equals( code ) )
				return c;
		}
		return null;
	}
